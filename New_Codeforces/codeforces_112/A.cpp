lo gcd(lo a, lo b)
{
	if(a == 0)return b;

	return gcd(b%a, a);
}

lo power(lo x, lo y)
{
	if(y == 0)return 1;
	lo temp = power(x, y/2);
	temp = (temp*temp)%MOD;
	if(1&y)return (x*temp)%MOD;

	return temp;
}

void dfs(lo start, vector<vector<lo>>&g, vector<lo>&visited)
{
	visited[start] = true;
	
	for(auto child : g[start])
	{
		if(!visited[child])
		dfs(child,g,visited);
	}
}


bool f(lo m, lo n)
{
	lo a = m/15, b = m/20, c = m/25;
	// debug(6*a+8*b+10*c);
	// debug(m);
	return (6*a+8*b+10*c >= n);
}

void solve()
{
	lo n; cin >> n;

	lo l = 0, r = 25*n, res = 25*n;

	while(l <= r)
	{
		lo m = (l + r) >> 1;

		if(f(m, n))
		{
			r = m - 1;
			res = min(res, m);
		}
		else
		{
			l = m + 1;
		}
	}

	cout << res << endl;
}

